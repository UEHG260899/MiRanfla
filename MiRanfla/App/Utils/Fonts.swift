import Foundation

enum Fonts {
    enum Name: String {
        case regular = "Nunito-Regular"
        case medium = "Nunito-Medium"
        case semibold = "Nunito-SemiBold"
        case bold = "Nunito-Bold"
    }

    enum Size: CGFloat {
        case title = 34
        case body = 17
        case description = 15
        case caption = 13
    }
}
